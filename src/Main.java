    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        setAllInvisible();
        Roads.setVisible(true);
        
        MapViewer v = new MapViewer(Double.parseDouble(jTextField8.getText()), Double.parseDouble(jTextField9.getText()));
        Roads.add(v, new org.netbeans.lib.awtextra.AbsoluteConstraints(440, 200, 500, 480));

        if (jCheckBox1.isSelected()) {
            String stringUrl;
            if (jCheckBox2.isSelected()) {
                String initialLat = jTextField8.getText();
                String initialLong = jTextField9.getText();
                String finalLat = jTextField7.getText();
                String finalLong = jTextField10.getText();
                
                System.out.println("Downloading the required Map...");
                stringUrl = "http://api.openstreetmap.org/api/0.6/map?bbox=" + initialLat + "," + initialLong + "," + finalLat + "," + finalLong;
            }
            
            else {
                System.out.println("Downloading the old Map...");
                stringUrl = "http://api.openstreetmap.org/api/0.6/map?bbox=11.54,48.14,11.543,48.145";
            }
            
            File file = new File("map.osm");
            
            if(!file.exists()){
                System.out.println("Downloading...");
            
                try {
                    URL url = new URL(stringUrl);
                    FileUtils.copyURLToFile(url, file);
                } catch (MalformedURLException ex) {
                    Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
                graph = new GraphHopperAPI(file);

                jLabel21.setText("MaxLon: " + graph.maxLon);
                jLabel22.setText("MinLat: " + graph.minLat);
                jLabel23.setText("MaxLat: " + graph.maxLat);
                jLabel24.setText("MinLon: " + graph.minLon);
            
        }
        
        
    }//GEN-LAST:event_jButton11ActionPerformed

